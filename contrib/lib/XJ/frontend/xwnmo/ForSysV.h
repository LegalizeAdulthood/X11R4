#define	FD_SET(bit, mask)	((*mask) |= (1 << bit))
#define	FD_ISSET(bit, mask)	((*mask) & (1 << bit))
#define	FD_ZERO(mask)		((*mask) = 0)
#define	FD_CLR(bit, mask)	((*mask) &= ~(1 << bit))
#ifdef	FD_SETSIZE
#	undef	FD_SETSIZE
#endif
#define	FD_SETSIZE	32
